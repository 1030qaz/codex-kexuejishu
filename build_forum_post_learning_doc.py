def split_posts(text: str) -> list[dict[str, str]]:
    parts = re.split(r"^=====\s*(.*?)\s*=====\s*$", text, flags=re.M)
    posts: list[dict[str, str]] = []
    for index in range(1, len(parts), 2):
        header = parts[index].replace("?", "楼")
        body = parts[index + 1].strip()
        match = re.match(r"(.+?)\s*\|\s*(.+)", header)
        floor = match.group(1).strip() if match else header.strip()
        posted_at = match.group(2).strip() if match else ""
        posts.append({"floor": floor, "time": posted_at, "body": body})
    return posts


def clean_nga_text(value: str) -> str:
    value = re.sub(r"\[quote\].*?\[/quote\]", "【引用内容已省略，保留后续回复学习点】", value, flags=re.S | re.I)
    value = re.sub(r"\[/?(?:size|color|b|i|u|collapse|align|url|del|table|tr|td|h)[^\]]*\]", "", value, flags=re.I)
    value = re.sub(r"\[img\](.*?)\[/img\]", r"【原帖配图：\1】", value, flags=re.I | re.S)
    value = re.sub(r"\[s:[^\]]+\]", "", value)
    value = re.sub(r"\./mon_\S+", "【原帖配图】", value)
    value = value.replace("&#160;", " ")
    value = value.replace("&#8838;", "包含但不等于")
    value = re.sub(r"\r\n?", "\n", value)
    value = re.sub(r"[ \t\f\v]+", " ", value)
    value = re.sub(r" *\n *", "\n", value)
    value = re.sub(r"\n{3,}", "\n\n", value)
    return value.strip()


