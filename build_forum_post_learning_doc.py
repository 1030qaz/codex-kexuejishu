    doc.add_paragraph(
        "原始文本包含 100-119 楼。学习价值集中在 101 楼的交易体系合集，"
        "112 楼和 118 楼提供了踏空、补仓、日内做 T 与仓位能力匹配的实战补充；"
        "其余楼层主要是打卡、欢迎、情绪反馈和秩序提醒。"
    )
    table = doc.add_table(rows=1, cols=4)
    table.style = "Table Grid"
    hdr = table.rows[0].cells
    hdr[0].text = "楼层"
    hdr[1].text = "时间"
    hdr[2].text = "内容类型"
    hdr[3].text = "学习处理"
    for post in posts:
        body = post["body"]
        row = table.add_row().cells
        row[0].text = post["floor"]
        row[1].text = post["time"]
        if post["floor"].startswith("101"):
            kind = "核心学习合集"
            action = "拆成复盘、做T、止盈止损、调仓、趋势、龙头风向标、半导体等模块"
        elif post["floor"].startswith(("112", "118")):
            kind = "实战问答"
            action = "提炼为踏空后补仓、仓位能力与做T边界"
        elif any(word in body for word in ["学习", "打卡", "前排", "后排"]):
            kind = "学习氛围"
            action = "保留为学习场景背景"
        elif any(word in body for word in ["举报", "炸弹"]):
            kind = "秩序提醒"
            action = "保留为论坛环境背景"
        else:
            kind = "情绪反馈"
            action = "不单独分析，只作为帖子语境"
        row[2].text = kind
        row[3].text = action


